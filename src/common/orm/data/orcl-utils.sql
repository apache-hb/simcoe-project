CREATE OR REPLACE FUNCTION IS_BLANK_STRING(text IN VARCHAR2) RETURN BOOLEAN IS
BEGIN
    IF REPLACE(REGEXP_REPLACE(text, '\s'), CHR(0)) IS NULL THEN
        RETURN TRUE;
    ELSE
        RETURN FALSE;
    END IF;
END;
/

CREATE TABLE IF NOT EXISTS IS_BLANK_STRING_TEST_CASES (
    INPUT CHARACTER VARYING(100),
    EXPECTED BOOLEAN NOT NULL,
    CONSTRAINT UQ_IS_BLANK_STRING_TEST_CASES_INPUT UNIQUE (INPUT)
);

TRUNCATE TABLE IS_BLANK_STRING_TEST_CASES;

INSERT INTO
    IS_BLANK_STRING_TEST_CASES (INPUT, EXPECTED)
VALUES
    (NULL, TRUE),
    ('', TRUE),
    (CHR(10), TRUE),
    (CHR(13), TRUE),
    (CHR(9), TRUE),
    (CHR(0), TRUE),
    ('a', FALSE);

SELECT
    RAWTOHEX(INPUT),
    IS_BLANK_STRING(INPUT) AS ACTUAL,
    EXPECTED,
    CASE WHEN IS_BLANK_STRING(INPUT) = EXPECTED THEN 'PASS' ELSE 'FAIL' END AS RESULT
FROM IS_BLANK_STRING_TEST_CASES;
